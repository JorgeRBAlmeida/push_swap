#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*src;

	i = 0;
	src = (unsigned char *)s;
	while (i < n && src[i] != (unsigned char)c)
		i ++;
	if (i == n)
		return (NULL);
	return ((unsigned char *)&src[i]);
}
